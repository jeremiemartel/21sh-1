#include "libft.h"

void	ft_mputendl(char const *s, int m)
{
	while (m--)
		write(1, " ", 1);
	if (s)
	{
		ft_putstr(s);
		ft_putstr(WHITE);
		ft_putchar('\n');
	}
}
